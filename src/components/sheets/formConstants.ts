// Helper function to get interests based on relationship, age, and gender
export const getInterestsForPerson = (
    relationship?: string,
    age?: number,
    gender?: string
): { primary: typeof menInterests; other?: typeof menInterests } => {
    // Determine if it's a child (12 and below)
    const isChild = age !== undefined && age <= 12;
    
    // Priority 1: Relationship-based (father = men, regardless of age)
    if (relationship === "father") {
        return { primary: menInterests };
    }
    if (relationship === "mother") {
        return { primary: womenInterests };
    }
    
    // Priority 2: Age-based for siblings/children (boys and girls are 12 and below)
    if (isChild) {
        if (relationship === "brother" || relationship === "son") {
            return { primary: boysInterests };
        }
        if (relationship === "sister" || relationship === "daughter") {
            return { primary: girlsInterests };
        }
    }
    
    // Priority 3: Gender-based (if explicitly provided and not a child)
    if (!isChild) {
        if (gender === "male") {
            return { primary: menInterests };
        }
        if (gender === "female") {
            return { primary: womenInterests };
        }
    }
    
    // Priority 4: Infer from relationship if gender not provided
    if (!gender) {
        if (relationship === "brother" || relationship === "son") {
            return isChild ? { primary: boysInterests } : { primary: menInterests };
        }
        if (relationship === "sister" || relationship === "daughter") {
            return isChild ? { primary: girlsInterests } : { primary: womenInterests };
        }
    }
    
    // Ambiguous gender - mix 50% top men's + 50% top women's, then "Other" with rest
    const topMen = menInterests.slice(0, 12); // Top 12 (50% of 24)
    const topWomen = womenInterests.slice(0, 12); // Top 12 (50% of 24)
    
    // Mix them by alternating
    const mixedPrimary: typeof menInterests = [];
    const maxLength = Math.max(topMen.length, topWomen.length);
    for (let i = 0; i < maxLength; i++) {
        if (i < topMen.length) mixedPrimary.push(topMen[i]);
        if (i < topWomen.length) mixedPrimary.push(topWomen[i]);
    }
    
    // Get remaining interests for "Other" (rest of men's + rest of women's, mixed)
    const remainingMen = menInterests.slice(12);
    const remainingWomen = womenInterests.slice(12);
    const other: typeof menInterests = [];
    const maxRemaining = Math.max(remainingMen.length, remainingWomen.length);
    for (let i = 0; i < maxRemaining; i++) {
        if (i < remainingMen.length) other.push(remainingMen[i]);
        if (i < remainingWomen.length) other.push(remainingWomen[i]);
    }
    
    return { primary: mixedPrimary, other };
};

